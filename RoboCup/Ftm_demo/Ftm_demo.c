int main (void)
{
	char ch;     

  	printf("\nRunning the Ftm_demo project.\n");
    LED0_Init();  
    FTM_PWM_Init(FTM2_BASE_PTR, FTM_PWMMODE_COMBINE, FTM_PWM_LOWTRUEPULSE);
    printf("\nPress any key to set the channel 0 and 1 PWM duty cycle.\n");
    in_char();
    FTM_Set_DutyCycle_Combine(FTM2_BASE_PTR, 1, 45);
	while(1)
	{
		ch = in_char();
		out_char(ch);
        LED0_Toggle();
	} 
}
